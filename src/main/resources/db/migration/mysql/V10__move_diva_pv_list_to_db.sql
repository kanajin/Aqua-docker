CREATE TABLE `diva_pv_info`
(
    `pv_id`             int(11) NOT NULL,
    `bpm`               int(11) NOT NULL,
    `song_name`         varchar(255) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
    `song_name_eng`     varchar(255) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
    `song_name_reading` varchar(255) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
    `arranger`          varchar(255) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
    `lyrics`            varchar(255) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
    `music`             varchar(255) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
    `performer_number`  int(11)                                 DEFAULT NULL
) ENGINE = InnoDB
  DEFAULT CHARSET = utf8mb4
  COLLATE = utf8mb4_unicode_ci;

CREATE TABLE `diva_pv_info_level`
(
    `id`      bigint(20) NOT NULL,
    `edition` int(11)    NOT NULL,
    `level`   varchar(255) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
    `version` int(11)    NOT NULL,
    `diff`    varchar(255) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
    `pv_id`   int(11)                                 DEFAULT NULL
) ENGINE = InnoDB
  DEFAULT CHARSET = utf8mb4
  COLLATE = utf8mb4_unicode_ci;

ALTER TABLE `diva_pv_info`
    ADD PRIMARY KEY (`pv_id`);

ALTER TABLE `diva_pv_info_level`
    ADD PRIMARY KEY (`id`),
    ADD CONSTRAINT `FK_pv_info_pvid` FOREIGN KEY (`pv_id`) REFERENCES `diva_pv_info` (`pv_id`);

ALTER TABLE `diva_pv_info_level`
    MODIFY `id` bigint(20) NOT NULL AUTO_INCREMENT;



